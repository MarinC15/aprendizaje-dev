# variables.py
# Ejemplos de variables y tipos de datos en Python

# Números enteros (int)
edad = 21

# Números decimales (float)
altura = 1.75

# Cadenas de texto (str)
nombre = "César"

# Valores booleanos (bool)
es_estudiante = True

# Listas (pueden contener varios valores)
colores = ["rojo", "verde", "azul"]

# Diccionarios (clave: valor)
persona = {
    "nombre": "César",
    "edad": 21,
    "altura": 1.75
}

# Mostramos los valores en consola
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("¿Es estudiante?", es_estudiante)
print("Colores favoritos:", colores)
print("Información de persona:", persona)
